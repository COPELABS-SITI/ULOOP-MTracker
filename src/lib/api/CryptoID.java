package lib.api;

import com.google.protobuf.ByteString;

public class CryptoID {

	private byte []cid;
	
	public CryptoID(ByteString bs)
	{
		cid = bs.toByteArray();
	}
	
	public byte[] getBytes()
	{
		return this.cid;
	}
}
